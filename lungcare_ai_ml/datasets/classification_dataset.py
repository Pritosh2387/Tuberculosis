"""
datasets/classification_dataset.py
────────────────────────────────────
CSV-driven chest X-ray classification dataset.

Returns dicts so the DataLoader can batch heterogeneous outputs::

    {
        "image":    FloatTensor (3, H, W),
        "label":    LongTensor scalar (class index),
        "metadata": dict with image_path, label_name, dataset
    }

CSV schema
----------
Required columns: ``image_path``, ``label``, ``label_idx``
Optional columns: ``dataset``, ``split``

Generated by ``scripts/prepare_data.py`` from Montgomery + Shenzhen.
Any dataset that produces the same CSV schema works as a drop-in.

Interview note
--------------
Why return a dict instead of a tuple?  Tuples are fragile — adding a
mask/metadata field requires changing every line that unpacks the tuple.
Dicts are extensible: adding ``"mask"`` is backward compatible.
"""
from __future__ import annotations

import logging
from pathlib import Path
from typing import Any

import cv2
import numpy as np
import pandas as pd
import torch
from torch.utils.data import Dataset

from datasets.transforms import build_transforms

logger = logging.getLogger("lungcare.dataset.classification")


def _load_image_rgb(path: Path) -> np.ndarray:
    """
    Load image from disk and return (H, W, 3) uint8 RGB array.

    OpenCV reads BGR by default — this function always converts to RGB.
    Greyscale X-rays are converted to 3-channel RGB because ImageNet-
    pretrained backbones expect 3-channel input.
    """
    img = cv2.imread(str(path), cv2.IMREAD_UNCHANGED)
    if img is None:
        raise RuntimeError(f"cv2.imread failed: {path}")

    # Normalise to uint8
    if img.dtype != np.uint8:
        img = (img / max(img.max(), 1) * 255).clip(0, 255).astype(np.uint8)

    # Convert to 3-channel RGB
    if img.ndim == 2:
        img = cv2.cvtColor(img, cv2.COLOR_GRAY2RGB)
    elif img.shape[2] == 4:
        img = cv2.cvtColor(img, cv2.COLOR_BGRA2RGB)
    else:
        img = cv2.cvtColor(img, cv2.COLOR_BGR2RGB)

    return img


class ClassificationDataset(Dataset):
    """
    Chest X-ray classification dataset driven by a CSV manifest.

    Args:
        csv_path:    Path to the split CSV file.
        split:       ``'train'``, ``'val'``, or ``'test'``.
        image_size:  Square resize target in pixels.
        num_classes: Expected number of classes (used only for validation).
        transform:   Optional pre-built Albumentations pipeline.
                     If None, ``build_transforms(split, image_size)`` is used.
    """

    def __init__(
        self,
        csv_path: str | Path,
        split: str = "train",
        image_size: int = 224,
        num_classes: int = 2,
        transform: Any = None,
    ) -> None:
        self.split       = split
        self.image_size  = image_size
        self.num_classes = num_classes
        self.transform   = transform or build_transforms(split, image_size)

        csv_path = Path(csv_path)
        if not csv_path.exists():
            raise FileNotFoundError(f"CSV not found: {csv_path}")

        self.df = pd.read_csv(csv_path)
        missing = {"image_path", "label", "label_idx"} - set(self.df.columns)
        if missing:
            raise ValueError(f"CSV missing required columns: {missing}")

        logger.info("ClassificationDataset | split=%s | samples=%d",
                    split, len(self.df))

    def __len__(self) -> int:
        return len(self.df)

    def __getitem__(self, idx: int) -> dict[str, Any]:
        row        = self.df.iloc[idx]
        image_path = Path(str(row["image_path"]))
        label_idx  = int(row["label_idx"])

        try:
            image_np = _load_image_rgb(image_path)
        except Exception as exc:
            logger.warning("Failed to load sample %d (%s): %s", idx, image_path, exc)
            image_np = np.zeros(
                (self.image_size, self.image_size, 3), dtype=np.uint8
            )

        result       = self.transform(image=image_np)
        image_tensor = result["image"]      # (3, H, W) float32

        return {
            "image": image_tensor,
            "label": torch.tensor(label_idx, dtype=torch.long),
            "metadata": {
                "image_path": str(image_path),
                "label_name": str(row["label"]),
                "dataset":    str(row.get("dataset", "")),
                "idx":        idx,
            },
        }

    # ── Helpers ───────────────────────────────────────────────────────────────

    def get_class_counts(self) -> dict[int, int]:
        """Return {label_idx: count} dict for computing class weights."""
        return dict(self.df["label_idx"].value_counts())

    def get_sample_weights(self) -> torch.Tensor:
        """
        Inverse-frequency per-sample weights for WeightedRandomSampler.

        Ensures minority classes are sampled as frequently as majority
        classes, addressing class imbalance without oversampling.
        """
        counts  = self.get_class_counts()
        weights = [1.0 / counts[int(r["label_idx"])]
                   for _, r in self.df.iterrows()]
        return torch.tensor(weights, dtype=torch.float)
